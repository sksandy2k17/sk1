#for loop by ranges
for i in range(10):     
    print(i)
for i in range(1,11):
    print(i)
for i in range(0,20,2):
    print(i)
#for loop by list
a=[1,3,5]
for i in a:
    print(i)
for i in a:
    if (i%2==0):
        break
    print(i)
#for loop by string
for i in "santhosh":
    if(i=='a'):
        break
    print(i)
    
